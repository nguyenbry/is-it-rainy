function generateRadixColorScale(
  name:
    | "gray"
    | "mauve"
    | "slate"
    | "sage"
    | "olive"
    | "sand"
    | "gold"
    | "bronze"
    | "brown"
    | "yellow"
    | "amber"
    | "orange"
    | "tomato"
    | "red"
    | "ruby"
    | "crimson"
    | "pink"
    | "plum"
    | "purple"
    | "violet"
    | "iris"
    | "indigo"
    | "blue"
    | "cyan"
    | "teal"
    | "jade"
    | "green"
    | "grass"
    | "lime"
    | "mint"
    | "sky",
) {
  const scale = Array.from({ length: 12 }, (_, i) => {
    const id = i + 1;
    return [id, `var(--${name}-${id})`];
  });

  return Object.fromEntries(scale) as {
    1: string;
    2: string;
    3: string;
    4: string;
    5: string;
    6: string;
    7: string;
    8: string;
    9: string;
    10: string;
    11: string;
    12: string;
  };
}
